void main() {
// For Loops

  /// In for there are there parts
  /// [Init]
  /// [condition checking]
  /// [increment, decrement]

for( int i =1; i<=10;  i++) {

if(i==6) {
  continue;
}
print(i);
}

  /// While  Loop
  ///
  /// condition is checked first
  ///
  /// [3 parts]
  /// [init] [conditional] [decrement, increment]
  ///

  int j = 0;
  while (j <= 10) {
    print(j);
    j++;
  }

  ///  Do While  Loop
  ///
  /// block of code is executed and condition is checked first
  ///
  /// [3 parts]
  /// [init] [conditional] [decrement, increment]
  ///
  int k =0;
  do {
print(k);
  } 
  while(k>0);


}
